# ---------------------------------------------------------------------------
# Packet encoder — pure Python, no external dependencies
# ---------------------------------------------------------------------------

def image_to_packets(img):
    """Convert a 28×28 binary image (list-of-lists of 0/1) to 98 hw packets.

    Packs two 2×2 blocks per byte as nibbles (low=first block, high=second),
    scanning row-major over 14 block-rows × 7 byte-columns.
    Mirrors the serial framing that mnist_loader.sv reads on data_in[7:0].
    """
    packets = []
    for br in range(14):
        for bc in range(0, 14, 2):
            r  = 2 * br
            c  = 2 * bc
            c2 = 2 * (bc + 1)
            # Low nibble: 2×2 block starting at (r, c)
            low  = (img[r][c]   | (img[r][c+1]   << 1) |
                    (img[r+1][c] << 2) | (img[r+1][c+1] << 3)) & 0xF
            # High nibble: 2×2 block starting at (r, c2)
            high = (img[r][c2]   | (img[r][c2+1]   << 1) |
                    (img[r+1][c2] << 2) | (img[r+1][c2+1] << 3)) & 0xF
            packets.append(low | (high << 4))
    assert len(packets) == 98
    return packets


