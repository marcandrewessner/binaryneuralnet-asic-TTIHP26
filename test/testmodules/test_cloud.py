# ---------------------------------------------------------------------------
# Hardware helpers — operate on the TT wrapper pins exposed by tb.v
# ---------------------------------------------------------------------------

async def reset_dut(dut):
    """Start the clock, assert and then release reset."""
    clock = Clock(dut.clk, 10, unit="us")
    cocotb.start_soon(clock.start())
    dut.ena.value    = 1
    dut.rst_n.value  = 0
    dut.ui_in.value  = 0
    dut.uio_in.value = 0
    await ClockCycles(dut.clk, 5)
    dut.rst_n.value = 1
    await ClockCycles(dut.clk, 2)


async def pulse_reset(dut):
    """Short reset between images; clock keeps running."""
    dut.rst_n.value  = 0
    dut.ui_in.value  = 0
    dut.uio_in.value = 0
    await ClockCycles(dut.clk, 3)
    dut.rst_n.value = 1
    await ClockCycles(dut.clk, 2)


async def stream_packets(dut, packets):
    """Stream 98 bytes via ui_in / uio_in[0] (data_in_clk).

    Each packet: hold data_in_clk high for 2 cycles, then low for 2 cycles.
    This gives a clean rising edge that the SRAM samples reliably.
    """
    for pkt in packets:
        dut.ui_in.value  = int(pkt)
        dut.uio_in.value = 0x01          # data_in_clk = 1
        await ClockCycles(dut.clk, 2)
        dut.uio_in.value = 0x00          # data_in_clk = 0
        await ClockCycles(dut.clk, 2)


def read_result(dut):
    """Decode (inference_done, number_o) from uo_out.

    uo_out[4]   = inference_done_o
    uo_out[3:0] = number_o
    """
    raw            = int(dut.uo_out.value)
    inference_done = (raw >> 4) & 1
    number_o       = raw & 0xF
    return inference_done, number_o


