# test_cloud.py — Standalone CI test for the MNIST BNN inference pipeline.
#
# Completely self-contained: no torch, no MNIST files, no external dependencies
# beyond cocotb.  All 10 test vectors are 28×28 binary MNIST images baked
# directly into this file as [[0/1, ...] * 28] * 28 grids.
#
# Preprocessing applied offline to generate the binary images:
#
#   model.preprocess  =  LearnableShift(shift=0.66676885) → QuantizeBinary
#                        i.e. pixel ← sign(pixel − 0.66676885) ∈ {−1, +1}
#                        then mapped to {0, 1} for hardware
#
# At test time, image_to_packets() converts each 28×28 binary image to the
# 98-byte stream that mnist_loader.sv expects: two 2×2 pixel blocks packed
# per byte as (high_nibble | low_nibble), scanned row-major over 14 block-rows.
#
# The expected hardware predictions were computed with the Python baremetal tree
# (Main_BareMetalTreeInference.py) fed with the exact same embeddings the BNN
# hardware produces.  The test asserts bit-exact hardware faithfulness to the
# trained model — not merely MNIST ground-truth accuracy.
#
# Testbench: tb  (tb.v → tt_um_maw_mnistbnn → project.v → main.sv)
# Pin mapping (project.v):
#   ui_in[7:0]   ← data_in[7:0]   8-bit pixel packet (two 2×2 blocks / nibble)
#   uio_in[0]    ← data_in_clk    rising edge strobes each packet into the loader
#   uo_out[3:0]  → number_o        predicted digit 0–9 (valid when done=1)
#   uo_out[4]    → inference_done  held high after pipeline completes

import cocotb
