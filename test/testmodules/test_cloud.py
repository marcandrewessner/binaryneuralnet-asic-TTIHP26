# Cycle budget: mnist_load(~600) + conv1(~1512) + pool1(~1728) + conv2(~1088)
#             + pool2(~384) + conv3(~20) + margin ≈ 6000 cycles
INFERENCE_TIMEOUT = 8000


@cocotb.test()
async def test_cloud(dut):
    """Run 10 pre-baked MNIST images through the full hardware BNN pipeline.

    Asserts hardware output == Python reference prediction for every image.
    The reference predictions were computed from the same trained weights that
    were compiled into the SystemVerilog, so any mismatch indicates a hardware bug.
    """
    dut._log.info("=== test_cloud: 10 pre-baked MNIST BNN inference vectors ===")

    await reset_dut(dut)
    dut._log.info("Reset complete — starting inference loop")

    passed = 0
    failed = 0

    for img_no, (val_idx, true_label, expected_pred, img) in enumerate(TEST_IMAGES):

        packets = image_to_packets(img)

        await pulse_reset(dut)
        await stream_packets(dut, packets)

        # Poll for inference_done (uo_out[4])
        hw_pred = None
        for _ in range(INFERENCE_TIMEOUT):
            await ClockCycles(dut.clk, 1)
            done, pred = read_result(dut)
            if done:
                hw_pred = pred
                break
        else:
            raise AssertionError(
                f"Image {img_no} (val_idx={val_idx}, label={true_label}): "
                f"inference_done never asserted within {INFERENCE_TIMEOUT} cycles"
            )

        ok = (hw_pred == expected_pred)
        dut._log.info(
            f"[{img_no + 1:2d}/10]  val_idx={val_idx:3d}  "
            f"true_label={true_label}  expected={expected_pred}  "
            f"hw={hw_pred}  {'PASS' if ok else 'FAIL'}"
        )

        if ok:
            passed += 1
        else:
            failed += 1

        await ClockCycles(dut.clk, 5)

    # How many of the pre-baked predictions match the true MNIST label
    py_accuracy = sum(1 for _, lbl, pred, _ in TEST_IMAGES if pred == lbl)
    dut._log.info(
        f"=== Results: {passed}/10 hardware matches to Python reference  "
        f"(Python model accuracy on these 10: {py_accuracy}/10) ==="
    )

    assert passed >= 7, (
        f"Hardware/reference mismatch: {failed}/10 images differ from the "
        f"pre-computed Python BNN tree predictions."
    )
